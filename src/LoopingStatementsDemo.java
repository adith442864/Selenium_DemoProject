
public class LoopingStatementsDemo {

	public static void main(String[] args) {
		
		int i =0;
		while(i<10) {
			System.out.println(i);
			i++;
			
		}
		
		System.out.println("----------");
		
		int j=10;
		do {
			System.out.println(j);
			j--;
		}while(j>=1);
		
		while(true) {
			System.out.println("welcome to java");
			break;
		}

		
		
		
		
		
		
	}
	
	

}
